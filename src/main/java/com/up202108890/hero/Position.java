package com.up202108890.hero;

public class Position {
    private int x;
    private int y;
    public Position(int x1,int y1){
        x=x1;
        y=y1;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public void setX(int x) {
        this.x = x;
    }
    public void setY(int y) {
        this.y = y;
    }

}
